public class D86 {

    public static void main(String[] args) {
        int ukuran = 5;
        int[] array = {10, 20, 30, 40, 50};

        System.out.println("Elemen-elemen dalam array :");
        for (int i = 0; i < ukuran; i++) {
            System.out.println("Elemen ke-" + (i + 1) + " : " + array[i]);
        }
    }
}
